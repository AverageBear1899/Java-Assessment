    //allows the staff member to view the orderlines for a specific order
    private void btnViewOrdersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewOrdersActionPerformed
        
        if(tblOrders.getSelectedRow() == -1)
        {
            lblMessage.setText("Select Order First");
        }
        else
        {
            DefaultTableModel model = (DefaultTableModel)tblOrders.getModel();
            
            String customerId = 
                    String.valueOf(model.getValueAt(tblOrders.getSelectedRow(), 0));
            
            int orderId = 
                    Integer.parseInt(String.valueOf(model.getValueAt(tblOrders.getSelectedRow(), 1)));
            
            Customer customerSelected = customers.get(customerId);
            
            Order actualOrder = customerSelected.getOrders().get(orderId);
            
            StaffViewOrderLines viewOrderLines = new StaffViewOrderLines(loggedInStaff, customerId, actualOrder);
            this.dispose();
            viewOrderLines.setVisible(true);
        }
    }//GEN-LAST:event_btnViewOrdersActionPerformed
