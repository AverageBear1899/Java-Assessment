    public StaffViewOrderLines(Staff staff, String customerId, Order actualOrder) {
        loggedInStaff = staff;
        initComponents();
        //populates the table with orderlines
        DefaultTableModel model = (DefaultTableModel)tblOrders.getModel();
        for(Map.Entry<Integer, OrderLine> entry : actualOrder.getOrderLines().entrySet())
        {
            Product orderedProduct = entry.getValue().getProduct();
            
            model.addRow(new Object[] 
            {customerId, orderedProduct.getProductId(), orderedProduct.toString(), 
            "Â£" + String.format("%.02f", entry.getValue().getLineTotal())});
        }
    }

